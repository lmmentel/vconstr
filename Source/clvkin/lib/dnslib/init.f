      subroutine fctrl
      implicit real*8 (a-h,o-z),integer(i-n)
      parameter(one = 1.d0)
c
      common/factor/fac(0:40),faci(0:40)
c
c**  calculate factorials and inverse of factorials
c
      fac(0) = one
      faci(0) = one
      do i = 1,40
        fac(i) = fac(i-1)*i
        faci(i) = one/fac(i)
      enddo
c
      return
      end
